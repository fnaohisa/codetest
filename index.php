          <div class="performer__content__text">
            <p class="performer__content__name u-for-pc">
              ジョン・ウィリアムズ<br>
              <span>JOHN WILLIAMS</span>
            </p>
            <p class="text--mid u-mt-2">
              ジョン・ウイリアムズは、50年以上もの間、活躍しているアメリカで最も成功している映画作曲家。<br>
              「スター・ウォーズ」全8作、「ハリー・ポッター」の最初の3作、「スーパーマン」「SAYURI」「ホーム・アローン」「やさしい本泥棒」などを含む100作以上の映画の音楽を作曲している。映画監督スティーヴン・スピルバーグとの45年に渡る芸術においての関係は、「シンドラーのリスト」「E.T.」「ジョーズ」「ジュラシック・パーク」「未知との遭遇」「インディ・ジョーンズ」「プライベート・ライアン」「リンカーン」「BFG」「ペンタゴン・ペーパーズ 最高機密文書」などハリウッドを代表するたくさんの映画を世に送り出した。<br>
              映画音楽以外の作曲も多く行っており、４つのオリンピック(1983夏ロサンゼルス、1988夏ソウル、1996夏アトランタ、2002冬ソルトレークシティ）のテーマ曲、２つの交響曲、協奏曲を作曲、ボストン・ポップス・オーケストラの音楽監督を14シーズン務め、現在も名誉指揮者として名を連ねる。<br>
              アカデミー作曲賞においては、今までに51度のノミネート、5度の受賞を誇り、7度のイギリスのアカデミー賞、24度のグラミー賞、４度のゴールデン・グローブ賞、そして５度のエミー賞を獲得。
              2003年、国際オリンピック委員会（IOC）から、ジョンのオリンピック運動への貢献に対して最も名誉なオリンピック 功労賞が贈られた。<br>
              2009年にはアメリカ政府から全米芸術勲章を授与、2016年には、アメリカ映画協会（AFI）より44回生涯功労賞を授与。作曲家として、初めての受賞となった。
            </p>
