      <h2 class="performer__title title u-text-bold">出演者・クリエイター</h2>
      
      <div class="performer__content">
        <h3 class="performer__content__title">作曲<span>Music</span></h3>
        <div class="performer__content__detail">
          <p class="performer__content__name u-for-sp">
            ジョン・ウィリアムズ<br>
            <span>JOHN WILLIAMS</span>
          </p>
          <p class="performer__content__tmb">
            <img src="img/performer_01.png" alt="ジョン・ウィリアムズ">
